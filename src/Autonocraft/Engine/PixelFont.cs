        public static float MeasureString(string text, float pixelSize) => text.Length * 6f * pixelSize;

        public static void DrawString(SpriteBatch sb, Texture2D whiteTexture, string text, float startX, float startY, float pixelSize, Color color, float alpha)
        {
            float curX = startX;
            Color drawCol = color * alpha;
            foreach (char c in text.ToUpperInvariant())
            {
                char lookup = Font.ContainsKey(c) ? c : ' ';
                byte[] rows = Font[lookup];

                for (int r = 0; r < 7; r++)
                {
                    byte rowVal = rows[r];
                    for (int col = 0; col < 5; col++)
                    {
                        if (((rowVal >> (4 - col)) & 1) == 1)
                        {
                            float xMin = curX + col * pixelSize;
                            float xMax = curX + (col + 1) * pixelSize;
                            float yMin = startY + r * pixelSize;
                            float yMax = startY + (r + 1) * pixelSize;

                            int px = (int)MathF.Round(xMin);
                            int py = (int)MathF.Round(yMin);
                            int w = Math.Max(1, (int)MathF.Round(xMax) - px);
                            int h = Math.Max(1, (int)MathF.Round(yMax) - py);

                            sb.Draw(whiteTexture, new Rectangle(px, py, w, h), drawCol);
                        }
                    }
                }
                curX += 6f * pixelSize;
            }
        }
